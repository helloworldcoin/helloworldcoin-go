package NumberUtil

func IsEquals(long1 uint64, long2 uint64) bool {
	return long1 == long2
}

func IsLessThan(long1 uint64, long2 uint64) bool {
	return long1 < long2
}

func IsLessEqualThan(long1 uint64, long2 uint64) bool {
	return long1 <= long2
}

func IsGreatThan(long1 uint64, long2 uint64) bool {
	return long1 > long2
}

func IsGreatEqualThan(long1 uint64, long2 uint64) bool {
	return long1 >= long2
}
